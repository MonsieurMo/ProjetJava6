package model;

public class Monster2 {
	
	private int i1;
	private int i2;
	
	public Monster2(int i1, int i2)
	{
		this.i1 = i1;
		this.i2 = i2;
	}
	
	public int getX()
	{
		return i1;
	}
	
	public int getY()
	{
		return i2;
	}
	
	public void setX(int x)
	{
		this.i1 = x;
	}
	
	public void setY(int y)
	{
		this.i2 = y;
	}
	

}
